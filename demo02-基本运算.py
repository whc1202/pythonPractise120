import pandas as pd
# 2.5
print(5 / 2)
# 丢弃余数，只保留商
print(5 // 2)
# 求余数
print(5 % 2)
# 5的3次方
print(5 ** 3)
# 返回True
print(5 > 3)
# 返回False
print(5 < 3)
# python支持连续比较
print(2 < 3 < 5)
# 与上面相同含义
print(20 < 15 and 30 > 15)
# False
print(not 20 < 15)

# 总结
#     python的平方表示法是两个**
#     条件并列时用的是and
#     取非时用的是not
